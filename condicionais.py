# if | elif      | else
# se | se não se | se não

proximo_passo = input("se quiser entrar digite [E]ntrar, se quiser sair digite [S]air: ")

if (proximo_passo == "E"):
    print("Entrar")
elif (proximo_passo == "S"):
    print("Sair")
else:
    print("Você não digitou nem 'E' nem 'S'.")

